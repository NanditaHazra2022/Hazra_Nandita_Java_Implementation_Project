package SetImplementation;

/* An enum type is
* a special data type
* that enables for a variable
* to be a set of
* predefined constants. */

// Enum type of days of week.
public enum Day
{
    /* As the days of week
    * are constants,
    * they are in uppercase. */
    MONDAY,
    TUESDAY,
    WEDNESDAY,
    THURSDAY,
    FRIDAY,
    SATURDAY,
    SUNDAY
}
